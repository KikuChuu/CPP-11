#ifndef __BASIC_FSTREAM_H__
#define __BASIC_FSTREAM_H__
template<typename C, typename Tr = char_traits<C>>
class basic_fstream
  : public basic_iostream<C,Tr> {
public:
  using char_type = C;
  using int_type = Tr::int_type;
  using pos_type = Tr::pos_type;
  using off_type = Tr::off_type;
  using traits_type = Tr;
};
#endif
